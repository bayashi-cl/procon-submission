include byslib/core

proc main =
  let
    n, m = read(int)
    a = readSeq(m, int).toHashSet()

  var ans, st = newSeq[int]()

  for i in 1..n:
    st.add(i)
    if not (i in a):
      while st.len > 0:
        ans.add(st.pop())

  print(ans)


